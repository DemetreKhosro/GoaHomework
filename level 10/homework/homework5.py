#მომხმარებელს შემოაქვს ინფორმაცია
age = int(input("Enter your age: "))
years = int(input("Enter the amount of years: "))
#ახლა გამოვთვლით თუ რამდენი წლის იქნება მომხმარებელი მისი შემოყვანილი წლების რაოდენობის შემდეგ ცვლადი EA-თი (Estimated Age)
EA = age + years
print(str(years) + " " + "წლის შემდეგ, თქვენ იქნებით" + " " + str(EA) + " " + "წლის")