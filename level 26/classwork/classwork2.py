name = "DemeTre KhosroShviLi"
result = ""
for char in name:
    if char == "D":
        result += "d"
    elif char == 'K':
        result += 'k'
    elif char == 'T':
        result += 't'
    elif char == 'S':
        result += 's'
    elif char == 'L':
        result += 'l'
    else:
        result += char

